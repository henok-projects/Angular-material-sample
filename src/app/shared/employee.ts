export class Employee {
  constructor(
    public id: number,
    public fullName: string,
    public email: string,
    public salary: number,
    public experiance: number,
    public department: string
  ) {
    id = this.id;
    fullName = this.fullName;
    email = this.email;
    salary = this.salary;
    experiance = this.experiance;
    department = this.department;
  }
}
